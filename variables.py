# integer data type
print("Integer value:")
a=12345
print(a)
print("\n")
# character data type
print("Character:")
b='D'
c="E"
print(b)
print(c)
print("\n")

#String
print("String:")
d="Yash Rajankar"
print(d)
print("\n")

#Float
print("Float:")
e=3.14
print(e)
print("\n")

# When two or more data type is same we can concatenate them
print("Combination of two characters:")
print("Concatenate string1 & string2:")
string1="Yash"
string2="Rajankar"
comb1=string1 + string2
print(comb1)
print("\n")

