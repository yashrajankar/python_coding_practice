#Explicit Typecasting

#Typecasting into inter
print("Typecasting into inter")
a = int(5.7)
b = int(6)
c = int("9")
print(a)
print(b)
print(c)
result= a+b+c
print("Result=",result)
print("\n")

#Typecasting into floating value
print("Typecasting into floating value")
a = float(5.7)
b = float(6)
c = float("9")
print(a)
print(b)
print(c)
result= a+b+c
print("Result=",result)
print("\n")

#Typecasting into string
print("Typecasting into string")
a = str("5.7")
b = str(6)
c = str("9")
print(a)
print(b)
print(c)
result= a+b+c
print("Result=",result)
print("\n")